    public void fillTable(ArrayList<Grade> displayList) {
        model.setRowCount(0);
        for (Grade grade : displayList) {
            Object[] row = {grade.getStudentCode(), grade.getEngScore(), grade.getItScore(), grade.getSportScore(), grade.getAvgScore()};
            model.addRow(row);
        }
    }


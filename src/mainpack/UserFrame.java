    public void showCurrentChoice(int index) {
        Grade gr = service.getGradeLists().get(index);
        String nameStu = GradeDAO.getInstance().getNameStudent(gr.getStudentCode());
        nameDisplay.setText(nameStu);
        codeStuInput.setText(gr.getStudentCode());
        codeStuInput.setEditable(false);
        engInput.setText(String.valueOf(gr.getEngScore()));
        itInput.setText(String.valueOf(gr.getItScore()));
        sportInput.setText(String.valueOf(gr.getSportScore()));
        avgScroreDisplay.setText(String.valueOf(gr.getAvgScore()));
        displayTable.setRowSelectionInterval(currentIndex, currentIndex);
        currentIndexGrade.setText("Record: " + (index + 1) + " / " + service.getGradeLists().size());
    }

    private Grade inputValidation() {
        String stucode = "";
        float engSc = 0;
        float itSc = 0;
        float sportSc = 0;
        int countErr = 0;

        if (codeStuInput.getText().isEmpty()) {
            countErr++;
            codeErr.setText("Bạn Phải Nhập Vào Mã Sinh Viên");
        } else if (!codeStuInput.getText().startsWith("PH")) {
            countErr++;
            codeErr.setText("Mã Sinh Viên Sai Cú Pháp");

        } else if (service.checkValidStudentCode(codeStuInput.getText())) {
            countErr++;
            codeErr.setText("Mã Sinh Viên Không Tồn Tại");
        } else {
            stucode = codeStuInput.getText();
            codeErr.setText("");
        }

        if (engInput.getText().isEmpty()) {
            countErr++;
            engError.setText("Bạn Phải Nhập Vào Điểm Tiếng Anh");
        } else {
            try {
                engSc = Float.parseFloat(engInput.getText());
                engError.setText("");
            } catch (NumberFormatException e) {
                countErr++;
                engError.setText("Bạn Nhập Sai Cú Pháp");
            }
        }

        if (itInput.getText().isEmpty()) {
            countErr++;
            itErr.setText("Bạn Phải Nhập Vào Điểm Tiếng Anh");
        } else {
            try {
                itSc = Float.parseFloat(itInput.getText());
                itErr.setText("");
            } catch (NumberFormatException e) {
                countErr++;
                itErr.setText("Bạn Nhập Sai Cú Pháp");
            }
        }

        if (sportInput.getText().isEmpty()) {
            countErr++;
            sportErr.setText("Bạn Phải Nhập Vào Điểm Tiếng Anh");
        } else {
            try {
                sportSc = Float.parseFloat(sportInput.getText());
                sportErr.setText("");
            } catch (NumberFormatException e) {
                countErr++;
                sportErr.setText("Bạn Nhập Sai Cú Pháp");
            }
        }

        if (countErr == 0) {
            Grade gr = new Grade(stucode, engSc, itSc, sportSc);
            Float avgScore = gr.getAvgScore();
            avgScroreDisplay.setText(String.valueOf(avgScore));
            return gr;
        }
        return null;
    }

